/*
 * AppReducer
 *
 * The reducer takes care of our data. Using actions, we can change our
 * application state.
 * To add a new action, add it to the switch statement in the reducer function
 *
 * Example:
 * case YOUR_ACTION_CONSTANT:
 *   return state.set('yourStateVariable', true);
 */

import { fromJS } from 'immutable';

import {
  LOAD_REPOS_SUCCESS,
  LOAD_REPOS,
  LOAD_REPOS_ERROR,
  LOAD_METADATA_ERROR,
  LOAD_METADATA_SUCCESS,
  LOAD_BUSINESS_PROCESSES_SUCCESS,
  LOAD_BUILD_NUMBER_SUCCESS,
} from './constants';

// The initial state of the App
const initialState = fromJS({
  loading: false,
  error: false,
  currentUser: false,
  userData: {
    repositories: false,
  },
  metaData: false,
  businessProcesses: false,
  buildNumber: false,
});

function appReducer(state = initialState, action) {
  switch (action.type) {
    case LOAD_REPOS:
      return state
        .set('loading', true)
        .set('error', false)
        .setIn(['userData', 'repositories'], false);
    case LOAD_REPOS_SUCCESS:
      return state
        .setIn(['userData', 'repositories'], action.repos)
        .set('loading', false)
        .set('currentUser', action.username);
    case LOAD_REPOS_ERROR:
      return state
        .set('error', action.error)
        .set('loading', false);
    case LOAD_METADATA_SUCCESS:
      return state
        .set('metaData', action.metaData);
    case LOAD_METADATA_ERROR:
      return state
        .set('error', action.error);
    case LOAD_BUSINESS_PROCESSES_SUCCESS:
      return state
        .set('businessProcesses', action.businessProcesses);
    case LOAD_BUILD_NUMBER_SUCCESS:
      return state
        .set('buildNumber', action.buildNumber);
    default:
      return state;
  }
}

export default appReducer;
